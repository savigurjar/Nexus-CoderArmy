function getrestaurant(restaurants) {
  const root = document.getElementById("root");
  restaurants.forEach((restaurants) => {
    // create a card div ->
    // 1: Image
    /* 2: card contant -> div
    1: div -> card_header div ->  {name,rating}
    2: div -> card_footer  div -> {foodtype and price}
    3: div -> card_location div ->  {restaurant_location , distance}
    
    */

    // create a card div
    const card = document.createElement("div");
    card.classList.add("card"); //giving class name .card

    // create Image
    const image = document.createElement("img");
    image.src = `photos/${restaurants.image}.jpg`;

    // card content
    const Card_content = document.createElement("div");
    Card_content.classList.add("card-content");

    //card header
    const Card_header = document.createElement("div");
    Card_header.classList.add("card-header");

    const h3 = document.createElement("h3");
    h3.textContent = restaurants.name;

    const rate = document.createElement("span");
    rate.textContent = "Rating: " + restaurants.rating;
    rate.classList.add("rating"); // Add the class for styling

    Card_header.appendChild(h3);
    Card_header.appendChild(rate);

    //card footer
    const Card_footer = document.createElement("div");
    Card_footer.classList.add("card-footer");

    const food = document.createElement("span");
    food.textContent = restaurants.food_type;

    const price = document.createElement("span");
    price.textContent = "₹" + restaurants.Price_for_two;

    Card_footer.appendChild(food);
    Card_footer.appendChild(price);

    //card location
    const Card_location = document.createElement("div");
    Card_location.classList.add("card-location"); //class list se multiple class assign kr skte h

    const location = document.createElement("span");
    location.textContent = restaurants.location;

    const distance = document.createElement("span");
    distance.textContent = restaurants.Distance_from_Customer_house + "km";

    Card_location.appendChild(location);
    Card_location.appendChild(distance);

    Card_content.appendChild(Card_header);
    Card_content.appendChild(Card_footer);
    Card_content.appendChild(Card_location);

    card.appendChild(image);
    card.appendChild(Card_content);

    root.appendChild(card);
  });
}
