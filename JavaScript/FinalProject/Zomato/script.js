document.getElementById("applyFilter").addEventListener("click", () => {
  const selectedFilter = document.querySelector(
    'input[name="filterOption"]:checked'
  );

  // if (!selectedFilter) {
  //   console.log("No filter selected");
  //   return;
  // }

  const ans = selectedFilter.value;

  if (ans == "rating") {
    restaurants.sort((a, b) => b.rating - a.rating); // Descending order
  } else if (ans == "highLow") {
    restaurants.sort((a, b) => b.Price_for_two - a.Price_for_two);
  } else if (ans == "costLowHigh") {
    // Fixed comparison operator
    restaurants.sort((a, b) => a.Price_for_two - b.Price_for_two);
  } else if (ans == "distance") {
    restaurants.sort(
      (a, b) => a.Distance_from_Customer_house - b.Distance_from_Customer_house
    );
  }

  document.getElementById("root").replaceChildren(); // Clear previous results
  document.getElementById("filterPopup").classList.add("hidden");
  getrestaurant(restaurants); // Display sorted results
});

